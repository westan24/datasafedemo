select
  EMPLOYEES.EMPLOYEE_ID
  , EMPLOYEES.FIRST_NAME
  , EMPLOYEES.LAST_NAME
  , EMPLOYEES.EMAIL
  , EMPLOYEES.PHONE_NUMBER
  , EMPLOYEES.HIRE_DATE
  , EMPLOYEES.CARD_NO
  , DEPARTMENTS.DEPARTMENT_ID
from
  DEMO.EMPLOYEES
  , DEMO.DEPARTMENTS
where
  EMPLOYEES.DEPARTMENT_ID = DEPARTMENTS.DEPARTMENT_ID and
  EMPLOYEES.EMPLOYEE_ID < 110
order by
  EMPLOYEES.EMPLOYEE_ID;
